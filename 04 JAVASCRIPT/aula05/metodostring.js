let nome = 'Igor Vasconcelos'
console.log(nome[0]);
console.log(nome.length); // retorna a quantidade de caracteres presentes na strings

console.log(nome.charAt(7)); // CharAt acessa determinado caractere baseado no índice escolhido

console.log(nome.toLowerCase()); // Transformar todos os caracteres em minúsculo

console.log(nome.toUpperCase()); // Transforma todos os caracteres em maiúsculo

console.log(nome.substring()); // corta a string a partir do primeiro índice até o segundo índice 

console.log(nome.split(' ')); // split separa a string de acordo com o que você especificou 
