//Adição
function adicao(num1, num2) {
    return num1 + num2 
}

//Subtração 
function subtracao(num1, num2) {
    return num1 - num2 
}

//Multiplicação 
function multiplicacao(num1, num2) {
    return num1 * num2
} 

//Divisão
function divisao(num1, num2) {
    return num1 / num2
}

console.log(`Adição: ${adicao(20,10)} `);
console.log(`Subtração: ${subtracao(20,10)} `)
console.log(`multiplicação: ${multiplicacao(20,10)} `)
console.log(`Divisão: ${divisao(20,10)} `)